package com.company;

public class Dimension {

    private int width; // largura
    private int lebgth; // comprimento

    public Dimension(int width, int lebgth) {
        this.width = width;
        this.lebgth = lebgth;
    }

    public int getWidth() {
        return width;
    }

    public int getLebgth() {
        return lebgth;
    }
}
